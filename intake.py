# -*- coding: utf-8 -*-
"""
デジトレ 摂取量の計算（機能③）

このファイルは「食べたものの栄養を算出する担当」です。
食品データ（foods.csv）を読み込み、食べたもの（食品名＋量g）から
合計カロリー・PFC（P=タンパク質・F=脂質・C=炭水化物）を計算する。

設計方針：
  ・食品データはコードに直書きせず、外の foods.csv に置く（＝データとロジックの分離）。
    → 食品を増やしたいときは CSV に行を足すだけ。このコードは触らなくてよい。
  ・CSVの値は「100gあたり」。食べた量(g)に応じて 量÷100 を掛けて換算する。
  ・計算結果は丸めない（正確な数値を返し、丸めるのは表示する側の役目）。

くわしい仕様は SPEC.md（第5章）を参照してください。
"""

import sys
import os
import csv  # Python標準の「CSVファイルを読み書きする道具箱」

# Windowsの画面で日本語が文字化けしないように出力をUTF-8にする。
# （targets.py 先頭と同じおまじない。このファイルを単体で動かす時のために入れておく）
if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8")


# このファイルがある場所を基準に foods.csv の場所を決める。
# こうしておくと、どこから実行しても（作業フォルダが違っても）CSVを見つけられる。
#   __file__         = このファイル自身のパス
#   os.path.dirname  = そのフォルダ部分を取り出す
#   os.path.join     = フォルダ名とファイル名を正しくつないでパスを作る
_HERE = os.path.dirname(os.path.abspath(__file__))
DEFAULT_FOODS_CSV = os.path.join(_HERE, "foods.csv")


# =====================================================================
# 1. 食品データの読み込み
# =====================================================================

def load_foods(csv_path=DEFAULT_FOODS_CSV):
    """
    foods.csv を読み込み、「食品名 → 栄養（100gあたり）」の辞書にして返す。

    返す形（イメージ）:
        {
          "ごはん（精白米）": {"kcal": 156.0, "P": 2.5, "F": 0.3, "C": 37.1},
          "卵（全卵・生）":   {"kcal": 142.0, "P": 12.2, "F": 10.2, "C": 0.4},
          ...
        }
    こうしておくと、食品名をキーにして栄養を一発で引ける（辞書の便利なところ）。
    """
    foods = {}  # 結果を入れる空の辞書

    # open(...) でファイルを開く。encoding="utf-8" で日本語を正しく読む。
    # newline="" は CSV を読むときの定番のお作法（改行の扱いを安定させる）。
    # with を使うと、読み終わったら自動でファイルを閉じてくれる（閉じ忘れ防止）。
    with open(csv_path, encoding="utf-8", newline="") as f:
        # DictReader は「1行を、見出しをキーにした辞書」にして読んでくれる便利機能。
        #   例: {"食品名": "卵（全卵・生）", "kcal": "142", "P": "12.2", ...}
        reader = csv.DictReader(f)
        for row in reader:
            name = row["食品名"]
            # CSVの値は文字（"142"）として読まれるので、float() で数値に変換する。
            foods[name] = {
                "kcal": float(row["kcal"]),
                "P": float(row["P"]),
                "F": float(row["F"]),
                "C": float(row["C"]),
            }

    return foods


# =====================================================================
# 2. 摂取量の計算
# =====================================================================

def calculate_intake(items, foods=None):
    """
    食べたものの合計カロリー・PFCを計算して返す。

    引数:
        items : 食べたもののリスト。各要素は (食品名, 量g) のペア。
                例: [("ごはん（精白米）", 200), ("卵（全卵・生）", 50)]
        foods : 食品データの辞書（省略すると foods.csv を自動で読み込む）
    戻り値:
        合計を入れた辞書。例: {"kcal": ..., "P": ..., "F": ..., "C": ...}
        ※丸めない（正確な数値のまま返す）。
    """
    # foods を渡されなかったら、その場で CSV を読み込む。
    if foods is None:
        foods = load_foods()

    # 合計を入れる箱。最初はすべて 0。
    total = {"kcal": 0.0, "P": 0.0, "F": 0.0, "C": 0.0}

    for name, grams in items:
        # 知らない食品名なら、黙って無視せずエラーで知らせる（打ち間違いに気づける）。
        if name not in foods:
            raise ValueError(f"食品データにありません: {name}")

        per100 = foods[name]      # その食品の「100gあたり」の栄養
        ratio = grams / 100       # 食べた量を「100g基準」に対する倍率に直す

        # 各栄養を「100gあたりの値 × 倍率」で足し込む
        total["kcal"] += per100["kcal"] * ratio
        total["P"] += per100["P"] * ratio
        total["F"] += per100["F"] * ratio
        total["C"] += per100["C"] * ratio

    return total


# =====================================================================
# 動作確認用（このファイルを直接実行したときだけ動く）
# =====================================================================
if __name__ == "__main__":
    # 例：ごはん200g ＋ 鶏むね肉150g ＋ 卵1個(約50g) を食べた場合
    meal = [
        ("ごはん（精白米）", 200),
        ("鶏むね肉（皮なし・生）", 150),
        ("卵（全卵・生）", 50),
    ]

    result = calculate_intake(meal)

    print("===== 摂取量の計算（テスト） =====")
    print("食べたもの：")
    for name, grams in meal:
        print(f"  ・{name} {grams}g")
    print("-------------------------------------------")
    # 表示の瞬間に丸める（:.0f=整数、:.1f=小数第1位）
    print(f" 合計カロリー : {result['kcal']:.0f} kcal")
    print(f" P タンパク質 : {result['P']:.1f} g")
    print(f" F 脂質       : {result['F']:.1f} g")
    print(f" C 炭水化物   : {result['C']:.1f} g")
    print("===========================================")
